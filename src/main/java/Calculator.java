public class Calculator {

    public int adder(int a, int b) {
        return a + b;
    }

    public int subtractor(int a, int b) {
        return a - b;
    }

    public int dividor(int a, int b) {
        return  a / b;
    }

    public int multipication(int a, int b) {
        return a * b;
    }
}
